const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link"
  onBack: () => void
}) => (
  <>
    <ToolbarGroup>
      <Button data-style="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
)

export function SimpleEditor({slug}: {slug: string}) {
  const isMobile = useMobile()
  const windowSize = useWindowSize()
  const [fetchedContent, setFetchedContent] = useState<any>(null)
  const [isLoading, setIsLoading] = useState(true)
  const [mobileView, setMobileView] = React.useState<
    "main" | "highlighter" | "link"
  >("main")
  const toolbarRef = React.useRef<HTMLDivElement>(null)

  const {getToken} = useAuth()
  
  React.useEffect(() => {
    if (!slug) {
      setIsLoading(false)
      return
    }

    const fetchContent = async () => {
      try {
        const token = await getToken()
        const response = await axios.get(`${BACKEND_URL}/blog/getBlog`, {
          params: {slug: slug},
        })
        const content = response.data.content
        setFetchedContent(content)
        console.log("Fetched content:", content)
      } catch (error) {
        console.error("Failed to fetch content:", error)
        setFetchedContent(content)
      } finally {
        setIsLoading(false)
      }
    }
    
    fetchContent()
  }, [slug, getToken])

  // Initialize editor only after content is loaded
  const editor = useEditor({
    immediatelyRender: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": "Main content area, start typing to enter text.",
      },
    },
    extensions: [
      StarterKit,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      Underline,
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,

      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: (file) => handleImageUpload(file, { slug }),
        onError: (error) => console.error("Upload failed:", error),
      }),
      TrailingNode,
      Link.configure({ openOnClick: false }),
    ],
    content: fetchedContent || content, 
  }, [fetchedContent]) 

  // Auto-save functionality
  React.useEffect(() => {
    if (!editor) return

    const debouncedSave = debounce(() => {
      const content = editor.getJSON()
      updateEditorContentToDatabase(content)
    }, 1000) // Save after every 1s of inactivity

    editor.on("update", debouncedSave)

    return () => {
      editor.off("update", debouncedSave)
      debouncedSave.cancel()
    }
  }, [editor])

  const bodyRect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  })

  React.useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main")
    }
  }, [isMobile, mobileView])

  async function updateEditorContentToDatabase(content: any) {
    try {
      const token = await getToken()
      const title = extractTitle(content) || ""
      
      const response = await axios.post(`${BACKEND_URL}/blog/updateBlog`, {
        content: content,
        title: title,
        slug: slug
      }, {
        headers: {
          "Authorization": `Bearer ${token}`
        }
      })
      
      console.log("Content saved successfully")
    } catch (error) {
      console.error("Failed to save content:", error)
    }
  }

  // show loading state while fetching content TODO: loader add krna hai edhar 
  if (isLoading) {
    return (
      <div className="simple-editor-loading">
        <p>Loading editor...</p>
      </div>
    )
  }

  // dont render editor until content is fetcched
  if (!editor) {
    return (
      <div className="simple-editor-loading">
        <p>Initializing editor...</p>
      </div>
    )
  }

  return (
    <EditorContext.Provider value={{ editor }}>
      <Toolbar
        ref={toolbarRef}
        style={
          isMobile
            ? {
                bottom: `calc(100% - ${windowSize.height - bodyRect.y}px)`,
              }
            : {}
        }
      >
        {mobileView === "main" ? (
          <MainToolbarContent
            onHighlighterClick={() => setMobileView("highlighter")}
            onLinkClick={() => setMobileView("link")}
            isMobile={isMobile}
          />
        ) : (
          <MobileToolbarContent
            type={mobileView === "highlighter" ? "highlighter" : "link"}
            onBack={() => setMobileView("main")}
          />
        )}
      </Toolbar>

      <div className="content-wrapper">
        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content"
        />
      </div>
    </EditorContext.Provider>
  )
}